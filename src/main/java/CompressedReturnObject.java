public class CompressedReturnObject {
    private String message;
    private byte[] data;

    public CompressedReturnObject(String message, byte[] data) {
        this.message = message;
        this.data = data;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public byte[] getData() {
        return data;
    }

    public void setData(byte[] data) {
        this.data = data;
    }
}
